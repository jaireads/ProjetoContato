    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        try {
            
            
            String nome = jTextFieldNome.getText();
            long cpf = Long.parseLong(jTextFieldCpf.getText());
            
            Cliente cliente = new Cliente();
            cliente.setNome(nome);
            cliente.setCpf(cpf);
            
            
            
            
            for(int i = 0; i < jTableContato.getModel().getRowCount(); i++){
                Contato contato = new Contato();
                contato.setTipo(jTableContato.getModel().getValueAt(i, 1).toString());
                contato.setDescricao(jTableContato.getModel().getValueAt(i, 0).toString());
                
                cliente.getContatos().add(contato);
                
                
            }
            
            
            
            ContatosDao agenda = new ContatosDao();
            agenda.incluir(cliente);
            
            
            
            limparTela();
            jTextFieldNome.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Dados Incluidos com Sucesso");
            
        } catch (Exception erro) {
            JOptionPane.showInternalMessageDialog(rootPane, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

